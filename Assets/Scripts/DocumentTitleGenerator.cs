using UnityEngine;

public static class DocumentTitleGenerator
{
    private static string[] prefixes = { 
        "Приказ №", "Распоряжение №", "Циркуляр №", "Директива №", "Служебная записка №",
        "Межведомственный меморандум №", "Протокол о намерениях №", "Секретное постановление №", "Экстренное уведомление №" 
    };
    private static string[] subjects = { 
        "о немедленном", "касательно внесения изменений в", "об упразднении", "о тотальном запрете", "касательно регламентации",
        "о принудительной стандартизации", "о внеплановом пересмотре концепции", "об усилении контроля над", "о временном игнорировании"
    };
    private static string[] objects = { 
        "департамента по учёту скрепок.", "отдела по борьбе с хорошим настроением.", "процедуры чаепития.", 
        "использования красных чернил.", "комитета по надзору за кактусами.", "процесса размножения офисных слухов.",
        "алгоритма выбора дежурного по кофейнику.", "правил квантовой телепортации между отделами.", "графика полива фикуса.",
        "протокола приветствия начальства в коридоре."
    };
    private static string[] reasons = { 
        "в связи с ретроградным Меркурием.", "согласно параграфу 7-БИС.", "во избежание временных парадоксов.", 
        "по причине неустановленного происхождения.", "в рамках оптимизации всего и вся.", "потому что гладиолус.",
        "в целях повышения энтропии.", "согласно последним научным данным о влиянии лунных фаз.", "во исполнение устного приказа, полученного во сне."
    };

    public static string GenerateTitle()
    {
        string prefix = prefixes[Random.Range(0, prefixes.Length)] + Random.Range(100, 9999);
        string subject = subjects[Random.Range(0, subjects.Length)];
        string obj = objects[Random.Range(0, objects.Length)];
        string reason = reasons[Random.Range(0, reasons.Length)];

        return $"{prefix}\n{subject} {obj}\n{reason}";
    }
}