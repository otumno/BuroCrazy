public enum Emotion
{
    // --- Базовые эмоции ---
    Neutral,    // Нейтральное (стандартное состояние)
    Happy,      // Счастливое (успешно получил услугу)
    Sad,        // Грустное (уходит расстроенным)
    Confused,   // Сконфуженное (потерялся)
    Waiting,    // Ожидающее / Скучающее (в очереди)

    // --- Негативные эмоции ---
    Irritated,  // Раздраженное (хочет в туалет, долго ждет)
    Angry,      // Злое (для персонала, имеющего дело с нарушителем)
    Enraged,    // В ярости (когда клиент "срывается" и бегает по залу)
    Sly,        // Хитрое (пытается пройти без очереди, воришка)
    Scared,     // Испуганное (когда пойман охранником)
    
    // --- Нейтральные/Рабочие эмоции ---
    Thinking,   // Размышляющее (в процессе обслуживания у стойки)
    Working,    // Сосредоточенное (персонал выполняет работу)
    Relaxed,    // Расслабленное (персонал на перерыве на кухне)
    Stressed    // В стрессе (на грани срыва)
}